import math as Math

def multiplication(x,y):
    return x*y
    
def addition(x,y):
    return x+y
   
def subtraction(x,y):
    return x-y

def power(x,y):
    return Math.pow(x,y)

def modulus(x,y):
    return x%y

def division(x,y):
    return x/y

